"""domains.basketball_nba.memory_atlas_scouting_render — Markdown rendering for NBA scouting.

Internal module; imported only by memory_atlas_scouting.build_scouting().

Design:
  - Per-archetype scouting profiles (not pairwise — NBA has no Style_Matchups).
  - Synthesises: Archetype note (style, signature, population) + Trends overview
    (this archetype's share trajectory + league efficiency context).
  - BARE-STEM [[wikilinks]] so Obsidian resolves by filename globally.
  - No individual player names.  No edge / betting language.
  - F5-clean: stdlib only.

"""
from __future__ import annotations

import pathlib
from typing import Dict, List, Optional, Tuple

from scripts.platformkit.atlas.obsidian_emit import write_note


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _direction_label(direction: str) -> str:
    return {"rising": "Rising", "falling": "Falling", "stable": "Stable"}.get(direction, direction.title())


def _modern_nba_role(label: str) -> str:
    """Return a short descriptive phrase for the archetype's modern-NBA role."""
    _ROLES: Dict[str, str] = {
        "High-Usage Creator":  "primary offensive engine — initiates half-court sets, draws defensive attention",
        "Scoring Guard":       "volume scorer who stresses defenses via shot creation and basket attacks",
        "3-and-D Wing":        "spacing-and-stopping specialist — enables creators by threatening from the perimeter",
        "Stretch Big":         "floor-stretching big who pulls rim-protectors into the short roll or three-point line",
        "Rim-Running Big":     "lob target and cutter — converts on dunks and layups generated by guard creation",
        "Defensive Anchor":    "rim-protection and rebounding anchor — anchors team defensive rating",
        "Versatile Forward":   "two-way connective tissue — guards multiple positions and contributes across areas",
        "Playmaking Big":      "secondary playmaker from the high post — adds a unique ball-handling hub in the frontcourt",
        "Bench Contributor":   "rotation depth piece — provides reliable execution in a defined, limited role",
        "Low-Usage Connector": "off-ball glue — fills gaps, moves without the ball, and limits possessions consumed",
    }
    return _ROLES.get(label, "role player contributing within team system")


# ---------------------------------------------------------------------------
# Profile renderer
# ---------------------------------------------------------------------------

def render_profile(
    arch_label: str,
    arch_slug: str,
    style: str,
    signature_lines: List[str],
    population: str,
    typical_pos: str,
    trend: Optional[Tuple[float, float, str]],
    league_eff_note: str,
) -> str:
    """Render one Scouting/<Archetype>.md profile body."""

    # front-matter
    fm = (
        "---\n"
        f"archetype: {arch_label}\n"
        "tags:\n"
        "  - sport/nba\n"
        "  - scouting\n"
        f"  - archetype/{arch_slug}\n"
        "---\n"
    )

    # breadcrumb — bare-stem wikilinks
    breadcrumb = (
        "[[_Scouting_Index|← Scouting Index]] | "
        f"[[{arch_slug}|{arch_label}]] | "
        "[[_Archetypes_Index|Archetypes]] | "
        "[[_Trends_Overview|Trends]]"
    )

    # signature block
    sig_md = "\n".join(f"- {ln}" for ln in signature_lines) if signature_lines else "_No signature data._"

    # trend section
    if trend:
        fp, lp, direction = trend
        dir_label = _direction_label(direction)
        trend_body = (
            f"- **Direction:** {dir_label}\n"
            f"- **Share trajectory:** {fp:.1f}% (first season on record) → {lp:.1f}% (latest season)\n"
            f"- **Δ:** {lp - fp:+.1f}pp\n"
            f"\n{league_eff_note}"
        )
    else:
        trend_body = f"- Trend data unavailable for {arch_label}.\n\n{league_eff_note}"

    # modern role
    role_note = _modern_nba_role(arch_label)

    # sources
    sources_section = (
        "\n## Sources\n\n"
        f"- [[{arch_slug}|{arch_label} — Archetypes note]]\n"
        "- [[_Trends_Overview|NBA Archetype-Season Trends]]\n"
    )

    # footer tags
    footer = f"\n---\n#sport/nba #scouting #archetype/{arch_slug}\n"

    body = "\n".join([
        fm,
        f"# Scouting Profile: {arch_label}",
        "",
        breadcrumb,
        "",
        "## Style",
        "",
        style or "_Style description unavailable._",
        "",
        "## Signature (Classification Thresholds)",
        "",
        sig_md,
        "",
        "## Population",
        "",
        f"- **Players fitting this archetype:** {population}",
        f"- **Typical position(s):** {typical_pos}",
        "",
        "## Role in Modern NBA",
        "",
        role_note + ".",
        "",
        "## Trend (Archetype Share, Cross-Season)",
        "",
        f"*(Source: [[_Trends_Overview]])*",
        "",
        trend_body,
        sources_section,
        footer,
    ])
    return body


# ---------------------------------------------------------------------------
# Index renderer
# ---------------------------------------------------------------------------

def render_index(
    entries: List[Tuple[str, str, Optional[Tuple[float, float, str]]]],
    out_dir: pathlib.Path,
) -> pathlib.Path:
    """Write _Scouting_Index.md.

    entries: [(slug, label, trend_or_None)]
    """
    rows: List[str] = []
    for slug, label, trend in entries:
        if trend:
            fp, lp, direction = trend
            trend_cell = f"{_direction_label(direction)} ({fp:.1f}% → {lp:.1f}%)"
        else:
            trend_cell = "—"
        rows.append(f"| [[{slug}|{label}]] | {trend_cell} |")

    table = "\n".join(rows) if rows else "| — | — |"

    body = (
        "---\n"
        "type: scouting-index\n"
        "tags:\n"
        "  - sport/nba\n"
        "  - scouting\n"
        "  - atlas/index\n"
        "---\n\n"
        "# NBA Scouting Profiles — Index\n\n"
        "[[_Index|← NBA Index]] | "
        "[[_Archetypes_Index|Archetypes]] | "
        "[[_Trends_Overview|Trends]]\n\n"
        f"One synthesised scouting profile per archetype with a vault note.  "
        f"**Total profiles: {len(entries)}**\n\n"
        "| Profile | Trend (share trajectory) |\n"
        "|---------|-------------------------|\n"
        f"{table}\n\n"
        "---\n"
        "#sport/nba #scouting #atlas/index\n"
    )
    path = out_dir / "_Scouting_Index.md"
    return write_note(path, body)
