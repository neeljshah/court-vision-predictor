"""scripts/validation_harness_winprob.py — single-source validation harness.

Loads the canonical (snap, X, y) pool ONCE and scores arbitrary in-play winprob
pipeline variants on it. Used to triage orphan artifacts before wiring into the
live API path.

Honesty contract:
  - The on-disk .lgb / .joblib artifacts (v1, v6_hp, v7_bag5, v4_fouls,
    inplay_isotonic, inplay_dualcal) were ALL fit on the full 3,685-game pool.
    Predicting on the same pool gives an IN-SAMPLE Brier — leaked.
  - Per-fold Brier on this pool is also leaked because the artifacts saw the
    test rows during training.
  - The HONEST walk-forward gate lives in the meta JSON `wf_eval` blocks
    (iter68 v6_hp, iter71 meta_blend) — those were generated by re-fitting the
    same recipe on each fold's training slice using IDENTICAL split methodology
    to iter67. The harness imports those numbers verbatim.

What the harness actually verifies:
  1. Wiring correctness — does loading the .lgb produce predictions whose
     in-sample Brier matches the meta `in_sample.brier`?
  2. Composition correctness — when we stack v6_hp + iso + sigmoid + polarity
     using iter71's NNLS weights, does the resulting full-pool Brier track the
     meta_blend's claim?
  3. Ship decision — for each variant, the harness reports the meta's
     wf_eval mean Brier delta + per-fold deltas, applies the gate (Δ ≤ -0.002
     AND no fold regress > +0.003 vs the iter67 raw baseline per fold), and
     emits a SHIP / NO-SHIP verdict the operator can act on.

Output: data/cache/validation_harness_winprob.json + console table.

Usage:
    conda run --no-capture-output -n basketball_ai \
      python scripts/validation_harness_winprob.py
"""
