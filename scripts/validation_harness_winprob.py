# ── data loading ──────────────────────────────────────────────────────────────

def load_canonical_pool(force_rebuild: bool = False) -> pd.DataFrame:
    """Build the (game_id, snapshot, features, label) frame."""
    # Note: home_team_id is mixed int/str in build_rows (linescores int vs
    # season_games abbreviation fallback). We rebuild each run rather than
    # caching to parquet, which can't handle the mixed dtype cleanly.
    print("  building canonical pool from linescores + season_games + quarter_features ...",
          flush=True)
    ls = load_linescores()
    sg = load_season_games()
    qf = load_quarter_features_summaries()
    df = build_rows(ls, sg, qf)
    df["polarity_pregame"] = 1.0 - df["pregame_win_prob"].astype(float)
    # Normalize home_team_id to numeric where possible — string fallbacks
    # (team abbreviations) become NaN, which categoricals handle fine.
    df["home_team_id"] = pd.to_numeric(df["home_team_id"], errors="coerce")
    return df


def wf_splits(n: int, n_folds: int = N_FOLDS) -> List[Tuple[int, int, int]]:
    """Same expanding-window splits as oos_validate / iter68 / iter71."""
    min_train = int(n * 0.60)
    test_size = (n - min_train) // n_folds
    splits = []
    for f in range(n_folds):
        train_end = min_train + f * test_size
        test_start = train_end
        test_end = test_start + test_size if f < n_folds - 1 else n
        splits.append((train_end, test_start, test_end))
    return splits


# ── feature frame builders ────────────────────────────────────────────────────

def _frame(df: pd.DataFrame, cols: List[str]) -> pd.DataFrame:
    X = df[cols].copy()
    for c in CAT_COLS:
        if c in X.columns:
            X[c] = X[c].astype("category")
    return X


# ── variant definitions ───────────────────────────────────────────────────────

class _Cache:
    _v1: Dict[str, Any] = {}
    _v6: Dict[str, Any] = {}
    _v7_bag: Dict[str, List[Any]] = {}
    _v4: Dict[str, Any] = {}
    _iso: Dict[str, Any] = {}
    _dualcal: Dict[str, Any] = {}
    _blend_meta: Dict[str, Dict[str, Any]] = {}


def _load_booster(path: str):
    import lightgbm as lgb
    return lgb.Booster(model_file=path)


def _load_v1(snap: str):
    if snap not in _Cache._v1:
        _Cache._v1[snap] = _load_booster(
            os.path.join(MODELS, f"inplay_winprob_{snap.lower()}.lgb"))
    return _Cache._v1[snap]


def _load_v6(snap: str):
    if snap not in _Cache._v6:
        _Cache._v6[snap] = _load_booster(
            os.path.join(MODELS, f"inplay_winprob_{snap.lower()}_v6_hp.lgb"))
    return _Cache._v6[snap]


def _load_v7_bag(snap: str):
    if snap != "endQ2":
        return []
    if snap not in _Cache._v7_bag:
        bag = []
        for s in range(5):
            p = os.path.join(MODELS, f"inplay_winprob_endq2_v7_bag5_seed{s}.lgb")
            if os.path.exists(p):
                bag.append(_load_booster(p))
        _Cache._v7_bag[snap] = bag
    return _Cache._v7_bag[snap]


def _load_v4_fouls(snap: str):
    if snap != "endQ3":
        return None
    if snap not in _Cache._v4:
        p = os.path.join(MODELS, "inplay_winprob_endq3_v4_fouls.lgb")
        _Cache._v4[snap] = _load_booster(p) if os.path.exists(p) else None
    return _Cache._v4[snap]


def _load_iso(snap: str):
    if snap not in _Cache._iso:
        import joblib
        p = os.path.join(MODELS, f"inplay_isotonic_{snap.lower()}.joblib")
        if not os.path.exists(p):
            _Cache._iso[snap] = None
        else:
            obj = joblib.load(p)
            # iter62 wraps the IsotonicRegression in a dict bundle.
            _Cache._iso[snap] = obj.get("isotonic") if isinstance(obj, dict) else obj
    return _Cache._iso[snap]


def _load_dualcal(snap: str):
    if snap not in _Cache._dualcal:
        import joblib
        p = os.path.join(MODELS, f"inplay_dualcal_{snap.lower()}.joblib")
        _Cache._dualcal[snap] = joblib.load(p) if os.path.exists(p) else None
    return _Cache._dualcal[snap]


def _load_blend_meta(snap: str):
    if snap not in _Cache._blend_meta:
        p = os.path.join(MODELS, f"inplay_meta_blend_{snap.lower()}.json")
        with open(p) as f:
            _Cache._blend_meta[snap] = json.load(f)
    return _Cache._blend_meta[snap]


def _meta_feature_cols(snap: str, variant: str) -> List[str]:
    fname = f"inplay_winprob_{snap.lower()}_{variant}_meta.json" if variant \
            else f"inplay_winprob_{snap.lower()}_meta.json"
    with open(os.path.join(MODELS, fname)) as f:
        return list(json.load(f)["feature_cols"])


# Variant predictors take a DataFrame slice and return P(home_win) np.ndarray.

def predict_v1_raw(snap: str, df: pd.DataFrame) -> np.ndarray:
    cols = _meta_feature_cols(snap, "")
    p = _load_v1(snap).predict(_frame(df, cols))
    return np.clip(np.asarray(p, dtype=float), 0.0, 1.0)


def predict_v1_dualcal(snap: str, df: pd.DataFrame) -> np.ndarray:
    raw = predict_v1_raw(snap, df)
    dc = _load_dualcal(snap)
    if dc is None:
        return raw
    platt, iso = dc["platt"], dc["isotonic"]
    clipped = np.clip(raw, 1e-6, 1.0 - 1e-6)
    logit = np.log(clipped / (1.0 - clipped))
    p_platt = platt.predict_proba(logit.reshape(-1, 1))[:, 1]
    out = iso.predict(p_platt)
    return np.clip(out, 0.0, 1.0)


def predict_v6_hp(snap: str, df: pd.DataFrame) -> np.ndarray:
    cols = _meta_feature_cols(snap, "v6_hp")
    p = _load_v6(snap).predict(_frame(df, cols))
    return np.clip(np.asarray(p, dtype=float), 0.0, 1.0)


def predict_v7_bag5(snap: str, df: pd.DataFrame) -> np.ndarray:
    if snap != "endQ2":
        return np.full(len(df), np.nan)
    bag = _load_v7_bag(snap)
    if not bag:
        return np.full(len(df), np.nan)
    cols = _meta_feature_cols(snap, "v6_hp")  # v7_bag5 shares v6_hp schema
    X = _frame(df, cols)
    preds = np.stack([b.predict(X) for b in bag], axis=0)
    return np.clip(preds.mean(axis=0), 0.0, 1.0)


def predict_v4_fouls(snap: str, df: pd.DataFrame) -> np.ndarray:
    if snap != "endQ3":
        return np.full(len(df), np.nan)
    booster = _load_v4_fouls(snap)
    if booster is None:
        return np.full(len(df), np.nan)
    cols = _meta_feature_cols(snap, "v4_fouls")
    missing = [c for c in cols if c not in df.columns]
    if missing:
        # v4 fouls features (home_team_pfs_cum etc) are not in our canonical
        # pool — would require enriching from a fouls parquet. For now skip.
        return np.full(len(df), np.nan)
    p = booster.predict(_frame(df, cols))
    return np.clip(np.asarray(p, dtype=float), 0.0, 1.0)


def predict_meta_blend(snap: str, df: pd.DataFrame) -> np.ndarray:
    """Compose iter71 meta_blend using LOADED artifacts + analytic components.

    Components per snap (from iter71_inplay_meta_blend.py):
      endQ1: v6_hp, iso(v6_hp), sigmoid_margin, polarity_pregame
      endQ2: v6_hp, iso(v6_hp), v7_bag5, sigmoid_margin, polarity_pregame
      endQ3: v6_hp, iso(v6_hp), v4_fouls, sigmoid_margin, polarity_pregame

    Note: at training time iter71 used cross-fold iso. At inference we use the
    single iter62 isotonic fit on full pool — production-equivalent.
    """
    meta = _load_blend_meta(snap)
    weights = meta["weights"]
    p_v6 = predict_v6_hp(snap, df)
    iso = _load_iso(snap)
    p_iso = np.clip(iso.predict(p_v6), 1e-7, 1 - 1e-7) if iso is not None else p_v6.copy()
    sm = df["score_margin"].astype(float).values
    p_sig = 1.0 / (1.0 + np.exp(-sm / 6.0))
    p_poly = np.clip(df["polarity_pregame"].astype(float).values, 1e-6, 1 - 1e-6)

    components = {
        "v6_hp": p_v6,
        "iso": p_iso,
        "sigmoid_margin": p_sig,
        "polarity_pregame": p_poly,
    }
    if snap == "endQ2":
        components["v7_bag5"] = predict_v7_bag5(snap, df)
    if snap == "endQ3":
        v4 = predict_v4_fouls(snap, df)
        if np.isnan(v4).any():
            # Drop v4_fouls component entirely if features unavailable;
            # renormalize remaining weights.
            v4 = None
        components["v4_fouls"] = v4 if v4 is not None else None

    # Apply NNLS weights, skipping unavailable components.
    used: Dict[str, float] = {}
    for name, w in weights.items():
        if name in components and components[name] is not None:
            used[name] = float(w)
    if not used:
        return p_v6
    total = sum(used.values())
    if total <= 1e-9:
        # iter71 zero'd all weights for this snap — fall back to best_single.
        return components.get(meta.get("best_single_component", "v6_hp"), p_v6)
    out = np.zeros(len(df), dtype=float)
    for name, w in used.items():
        out += (w / total) * components[name]
    return np.clip(out, 0.0, 1.0)


# ── scoring ───────────────────────────────────────────────────────────────────

def _safe_brier(y: np.ndarray, p: np.ndarray) -> float:
    return float(np.mean((p - y) ** 2))


def _safe_log_loss(y: np.ndarray, p: np.ndarray) -> float:
    p = np.clip(p, 1e-6, 1.0 - 1e-6)
    return float(-(y * np.log(p) + (1 - y) * np.log(1 - p)).mean())


def score_variant_full_pool(name: str, snap: str, df: pd.DataFrame,
                            predict_fn: Callable[[str, pd.DataFrame], np.ndarray],
                            ) -> Dict[str, Any]:
    """Score one variant on the FULL pool (in-sample) + per WF test slice.

    Per-fold numbers are LEAKED because the underlying boosters saw the test
    rows at training time. Useful only for composition / wiring sanity checks.
    """
    sub = df[df["snapshot"] == snap].copy().reset_index(drop=True)
    if len(sub) == 0:
        return {"name": name, "n": 0, "skipped": True}
    y = sub["home_team_won"].astype(int).values
    try:
        p = predict_fn(snap, sub)
    except Exception as e:
        return {"name": name, "error": f"{type(e).__name__}: {e}"}
    if np.isnan(p).all():
        return {"name": name, "n": len(sub), "skipped_nan": True}

    valid = ~np.isnan(p)
    y_v = y[valid]
    p_v = p[valid]

    brier = _safe_brier(y_v, p_v)
    ll = _safe_log_loss(y_v, p_v)
    acc = float(((p_v >= 0.5).astype(int) == y_v).mean())
    calib = compute_calibration(y_v, p_v)
    drift = float(calibration_drift_score(calib))

    fold_brier: List[float] = []
    for (_, ts, te) in wf_splits(len(sub)):
        if te > ts:
            yy = y[ts:te]
            pp = p[ts:te]
            m = ~np.isnan(pp)
            if m.sum() < 1:
                fold_brier.append(float("nan"))
            else:
                fold_brier.append(_safe_brier(yy[m], pp[m]))
        else:
            fold_brier.append(float("nan"))

    return {
        "name": name,
        "n": int(valid.sum()),
        "n_total": int(len(sub)),
        "in_sample_brier": brier,
        "in_sample_log_loss": ll,
        "in_sample_acc": acc,
        "calibration_drift": drift,
        "per_fold_test_brier_leaked": fold_brier,
        "leaked": True,
    }


def gate_decision(meta_fold_briers: List[float], baseline_fold_briers: List[float],
                  mean_delta_required: float = -0.002,
                  per_fold_regress_tol: float = 0.003,
                  ) -> Dict[str, Any]:
    arr_m = np.array(meta_fold_briers, dtype=float)
    arr_b = np.array(baseline_fold_briers, dtype=float)
    deltas = (arr_m - arr_b).tolist()
    mean_delta = float(np.mean(arr_m) - np.mean(arr_b))
    worst_regress = float(max(deltas))
    return {
        "mean_brier_meta": float(np.mean(arr_m)),
        "mean_brier_baseline": float(np.mean(arr_b)),
        "mean_delta": mean_delta,
        "per_fold_delta": deltas,
        "worst_fold_regress": worst_regress,
        "passes_mean_gate": mean_delta <= mean_delta_required,
        "passes_per_fold_gate": worst_regress <= per_fold_regress_tol,
        "ship": (mean_delta <= mean_delta_required) and (worst_regress <= per_fold_regress_tol),
    }


def meta_wf_eval_for(snap: str, variant: str) -> Optional[List[float]]:
    fname_map = {
        "v6_hp": f"inplay_winprob_{snap.lower()}_v6_hp_meta.json",
        "meta_blend": f"inplay_meta_blend_{snap.lower()}.json",
    }
    p = os.path.join(MODELS, fname_map.get(variant, ""))
    if not os.path.exists(p):
        return None
    with open(p) as f:
        d = json.load(f)
    if variant == "v6_hp":
        wf = d.get("wf_eval") or {}
        return list(wf.get("fold_briers") or []) or None
    if variant == "meta_blend":
        return list(d.get("fold_briers_blend") or []) or None
    return None


# ── main ──────────────────────────────────────────────────────────────────────

def main(force_rebuild: bool = False) -> Dict[str, Any]:
    t0 = time.time()
    print("validation_harness_winprob.py — loading pool ...", flush=True)
    df = load_canonical_pool(force_rebuild=force_rebuild)
    print(f"  pool rows: {len(df)}, games: {df['game_id'].nunique()}", flush=True)

    variants: Dict[str, Callable[[str, pd.DataFrame], np.ndarray]] = {
        "v1_raw": predict_v1_raw,
        "v1_dualcal_current_prod": predict_v1_dualcal,
        "v6_hp": predict_v6_hp,
        "v7_bag5_endq2_only": predict_v7_bag5,
        "v4_fouls_endq3_only": predict_v4_fouls,
        "meta_blend_iter71": predict_meta_blend,
    }

    out: Dict[str, Any] = {
        "ran_at_utc": time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime()),
        "n_rows_per_snapshot": {},
        "honesty_notes": [
            "Per-fold numbers labeled 'leaked' are computed against artifacts "
            "trained on the FULL pool — they are NOT honest WF estimates.",
            "Ship decisions use the meta JSON `wf_eval` blocks (generated by "
            "iter68 / iter71 via re-fit-per-fold on identical WF splits to "
            "iter67). Those numbers are honest.",
            "Gate: mean Brier delta vs iter67 v1_raw baseline <= -0.002 AND "
            "no per-fold regression > +0.003.",
        ],
        "snapshots": {},
    }

    for snap in SNAPSHOTS:
        print(f"\n=== {snap} ===", flush=True)
        sub = df[df["snapshot"] == snap]
        out["n_rows_per_snapshot"][snap] = int(len(sub))

        snap_block: Dict[str, Any] = {"variants": {}, "ship_decisions": {}}
        for name, fn in variants.items():
            res = score_variant_full_pool(name, snap, df, fn)
            if res.get("skipped") or res.get("skipped_nan") or res.get("error"):
                print(f"  [{name}] skip: {res.get('error') or 'n/a'}", flush=True)
                snap_block["variants"][name] = res
                continue
            print(f"  [{name}] in-sample Brier={res['in_sample_brier']:.4f} "
                  f"acc={res['in_sample_acc']:.3f} "
                  f"per-fold(leaked)={[round(x, 4) for x in res['per_fold_test_brier_leaked']]}",
                  flush=True)
            snap_block["variants"][name] = res

        # Ship decisions from meta wf_eval (honest).
        baseline = ITER67_V1_RAW_FOLD_BRIER[snap]

        # v6_hp ship decision
        v6_wf = meta_wf_eval_for(snap, "v6_hp")
        if v6_wf and len(v6_wf) == len(baseline):
            d = gate_decision(v6_wf, baseline)
            d["source"] = "v6_hp_meta.wf_eval.fold_briers"
            snap_block["ship_decisions"]["v6_hp_vs_v1_raw"] = d
            print(f"  >> v6_hp wf_eval mean Δ={d['mean_delta']:+.4f}  "
                  f"worst_regress={d['worst_fold_regress']:+.4f}  "
                  f"ship={d['ship']}", flush=True)

        # meta_blend ship decision (vs v1 raw)
        mb_wf = meta_wf_eval_for(snap, "meta_blend")
        if mb_wf and len(mb_wf) == len(baseline):
            d = gate_decision(mb_wf, baseline)
            d["source"] = "meta_blend.fold_briers_blend"
            snap_block["ship_decisions"]["meta_blend_vs_v1_raw"] = d
            print(f"  >> meta_blend wf_eval mean Δ={d['mean_delta']:+.4f}  "
                  f"worst_regress={d['worst_fold_regress']:+.4f}  "
                  f"ship={d['ship']}", flush=True)

        # meta_blend ship decision (vs v6_hp) — does meta_blend add value on top?
        if mb_wf and v6_wf and len(mb_wf) == len(v6_wf):
            d2 = gate_decision(mb_wf, v6_wf)
            d2["source"] = "meta_blend.fold_briers_blend vs v6_hp.fold_briers"
            snap_block["ship_decisions"]["meta_blend_vs_v6_hp"] = d2
            print(f"  >> meta_blend vs v6_hp mean Δ={d2['mean_delta']:+.4f}  "
                  f"worst_regress={d2['worst_fold_regress']:+.4f}  "
                  f"ship={d2['ship']}", flush=True)

        # v1_dualcal honest WF (iter67) vs v1_raw
        iso = ITER67_V1_ISO_FOLD_BRIER[snap]
        d3 = gate_decision(iso, baseline)
        d3["source"] = "iter67_inplay_dualcal_results.json (isotonic ≡ dual to 1e-7)"
        snap_block["ship_decisions"]["v1_dualcal_vs_v1_raw"] = d3
        print(f"  >> v1_dualcal (iter67) mean Δ={d3['mean_delta']:+.4f}  "
              f"worst_regress={d3['worst_fold_regress']:+.4f}  "
              f"ship={d3['ship']}", flush=True)

        out["snapshots"][snap] = snap_block

    out["elapsed_s"] = round(time.time() - t0, 2)
    os.makedirs(CACHE, exist_ok=True)
    with open(OUT_JSON, "w") as f:
        json.dump(out, f, indent=2)
    print(f"\n  wrote {OUT_JSON} in {out['elapsed_s']}s", flush=True)
    return out


if __name__ == "__main__":
    main(force_rebuild="--rebuild" in sys.argv)
