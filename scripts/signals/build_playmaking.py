"""Wave 1 builder: per-player PLAYMAKING signal profile.

Sources (all verified against real schema):
  - player_tracking_features.parquet  (season-aggregate; 2021-22 thru 2024-25)
      ast_points_created, potential_ast, secondary_ast, passes_made_per_g,
      ast_per_pass, ast_pct, drive_passes, drive_ast, drive_ast_pct,
      drive_tov, drive_tov_pct, drives_per_g
  - atlas_player_playmaking_network.parquet  (season-aggregate; 2022-23 thru 2025-26)
      ast_to_tov, ast_pts_created, pnr_bh_poss_fraction, drive_tov_rate
  - bbref_advanced_extended.parquet  (season; 2024-25 thru 2025-26)
      ast_pct, tov_pct
  - hustle_features.parquet  (season; 2018-19 thru 2024-25)
      hustle_screen_assists  -> PnR screener role proxy
  - leaguegamelog_regular_season.parquet + playoffs  (per-game; 2025-26 season only)
      Used to compute LEAK-FREE rolling L10 AST, TOV, A:TO (shift(1) within player)

Key signals (registry domain player.playmaking):
  Season-aggregate (label leak_rule="season-agg", consumer=scouting/corr-model):
    ast_pts_created_pg   points generated by this player's passes per game
    potential_ast_pg     passes that could have been assists per game
    secondary_ast_pg     hockey assists (pass to the passer who assists) per game
    passes_made_pg       total passes made per game
    ast_per_pass         fraction of passes that become assists
    ast_pct_tracking     % of teammate FGM assisted by this player (tracking)
    ast_pct_bbref        same statistic from BBRef advanced (wider season coverage)
    tov_pct_bbref        turnover rate (% of possessions ending in TOV)
    drive_and_kick_pg    drives that end in teammate shot (drive_passes per game)
    drive_ast_per_drive  fraction of drives ending in teammate assist
    drive_tov_per_drive  fraction of drives ending in TOV
    pnr_bh_poss_share    PnR ball-handler possession fraction (from playmaking atlas)
    screen_ast_pg        hustle screen assists per game (screener role proxy)
    ato_season           season A:TO from playmaking atlas (ast_to_tov)
    ato_bbref            season A:TO derived from tracking ast + tov columns

  Rolling L10, PRIOR-GAMES-ONLY via shift(1)
  (label leak_rule="shift(1) within player, sorted by game_date"):
    ast_l10              rolling 10-game average AST (prior games only)
    tov_l10              rolling 10-game average TOV
    ato_l10              rolling 10-game average A:TO (ast / (tov + 1e-6))
    ast_vol_l10          rolling 10-game std-dev of AST (volatility)

Leak rule: season-aggregate signals are scouting (consumer A/B); rolling L10 uses
shift(1) so the as-of-date row never contains the current game's stats.

Entity: player  |  Key: player_id  |  Output: data/cache/signals/playmaking.parquet

  python scripts/signals/build_playmaking.py
"""
from __future__ import annotations

import os
import warnings
import numpy as np
import pandas as pd

ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
CACHE = os.path.join(ROOT, "data", "cache")
OUT_DIR = os.path.join(CACHE, "signals")
OUT = os.path.join(OUT_DIR, "playmaking.parquet")

# Minimum games played floor to avoid noise in season-agg signals
MIN_GP_SEASON = 10
# Rolling window
L10 = 10


# ---------------------------------------------------------------------------
# Season-aggregate layer
# ---------------------------------------------------------------------------

def _load_tracking_season() -> pd.DataFrame:
    """Official NBA tracking: passing + driving stats per (player_id, season)."""
    path = os.path.join(CACHE, "player_tracking_features.parquet")
    df = pd.read_parquet(path)
    # Keep only players with >= MIN_GP_SEASON games
    df = df[df["drives_gp"] >= MIN_GP_SEASON].copy()
    keep = [
        "player_id", "player_name", "season",
        "drives_gp",
        "ast_points_created",   # ast_pts_created_pg
        "potential_ast",        # potential_ast_pg
        "secondary_ast",        # secondary_ast_pg
        "passes_made_per_g",    # passes_made_pg
        "ast_per_pass",
        "ast_pct",              # ast_pct_tracking (fraction)
        "drive_passes",         # drives_per_g partner for drive_and_kick
        "drive_ast",            # drive ast per game
        "drive_ast_pct",        # fraction of drives -> ast
        "drive_tov",            # drive_tov per game
        "drive_tov_pct",        # fraction of drives -> tov
        "drives_per_g",
    ]
    df = df[[c for c in keep if c in df.columns]].copy()
    df.rename(columns={
        "ast_points_created": "ast_pts_created_pg",
        "potential_ast": "potential_ast_pg",
        "secondary_ast": "secondary_ast_pg",
        "passes_made_per_g": "passes_made_pg",
        "ast_pct": "ast_pct_tracking",
        "drive_passes": "drive_and_kick_pg",
        "drive_ast": "drive_ast_per_g",
        "drive_ast_pct": "drive_ast_per_drive",
        "drive_tov": "drive_tov_per_g",
        "drive_tov_pct": "drive_tov_per_drive",
    }, inplace=True)
    return df


def _load_atlas_season() -> pd.DataFrame:
    """Playmaking network atlas: ast_to_tov, pnr share, per (player_id, season)."""
    path = os.path.join(CACHE, "atlas_player_playmaking_network.parquet")
    df = pd.read_parquet(path)
    keep = [
        "player_id", "season",
        "ast_to_tov",           # ato_season
        "ast_pts_created",      # same metric, atlas version
        "pnr_bh_poss_fraction", # PnR ball-handler share
        "drive_tov_rate",       # drive_tov per drive (continuous)
        "n_games",
    ]
    df = df[[c for c in keep if c in df.columns]].copy()
    df = df[df["n_games"] >= MIN_GP_SEASON].copy()
    df.rename(columns={
        "ast_to_tov": "ato_season",
        "ast_pts_created": "atlas_ast_pts_created_pg",
        "pnr_bh_poss_fraction": "pnr_bh_poss_share",
        "drive_tov_rate": "drive_tov_rate_atlas",
    }, inplace=True)
    return df


def _load_bbref_season() -> pd.DataFrame:
    """BBRef advanced: ast_pct, tov_pct per (player_id, season)."""
    path = os.path.join(CACHE, "bbref_advanced_extended.parquet")
    df = pd.read_parquet(path)
    # player_id may be float (NaN for unresolved); drop those
    df = df[df["player_id"].notna()].copy()
    df["player_id"] = df["player_id"].astype(int)
    keep = ["player_id", "season", "ast_pct", "tov_pct"]
    df = df[[c for c in keep if c in df.columns]].copy()
    df.rename(columns={
        "ast_pct": "ast_pct_bbref",
        "tov_pct": "tov_pct_bbref",
    }, inplace=True)
    return df


def _load_hustle_season() -> pd.DataFrame:
    """Hustle: screen_assists per game as PnR-screener role proxy."""
    path = os.path.join(CACHE, "hustle_features.parquet")
    df = pd.read_parquet(path)
    df = df[df["hustle_games_played"] >= MIN_GP_SEASON].copy()
    keep = ["player_id", "player_name", "season", "hustle_screen_assists"]
    df = df[[c for c in keep if c in df.columns]].copy()
    df.rename(columns={"hustle_screen_assists": "screen_ast_pg"}, inplace=True)
    return df


# ---------------------------------------------------------------------------
# Rolling L10 layer (per-game gamelog, shift(1) = prior-games-only)
# ---------------------------------------------------------------------------

def _load_gamelog_rolling() -> pd.DataFrame:
    """Compute leak-free rolling L10 AST, TOV, A:TO from per-game gamelogs.

    Leak rule: shift(1) within each player, sorted ascending by game_date.
    The current game's stats are NOT included in that game's rolling value.
    Only the most recent row per player is kept (as-of final game date).
    """
    reg = pd.read_parquet(
        os.path.join(CACHE, "cv_fix", "leaguegamelog_regular_season.parquet")
    ).copy()
    plo = pd.read_parquet(
        os.path.join(CACHE, "cv_fix", "leaguegamelog_playoffs.parquet")
    ).copy()
    # Tag season type so we can compute a regular-season-only season A:TO below
    # (fallback for players absent from the playmaking atlas, e.g. rookies).
    reg["_is_playoff"] = 0
    plo["_is_playoff"] = 1
    gl = pd.concat([reg, plo], ignore_index=True)
    gl["game_date"] = pd.to_datetime(gl["GAME_DATE"])
    gl.rename(columns={
        "PLAYER_ID": "player_id",
        "PLAYER_NAME": "player_name",
        "AST": "ast",
        "TOV": "tov",
        "MIN": "min",
    }, inplace=True)
    # Keep only rows with meaningful minutes (>0) to avoid DNP rows
    gl = gl[gl["min"] > 0].copy()
    gl.sort_values(["player_id", "game_date"], inplace=True)

    rows = []
    for pid, g in gl.groupby("player_id", sort=False):
        g = g.sort_values("game_date").reset_index(drop=True)
        # shift(1) = prior games only (leak-free)
        g["ast_l10"] = g["ast"].shift(1).rolling(L10, min_periods=3).mean()
        g["tov_l10"] = g["tov"].shift(1).rolling(L10, min_periods=3).mean()
        g["ast_vol_l10"] = g["ast"].shift(1).rolling(L10, min_periods=5).std()
        # A:TO rolling: compute over the rolling window sums (not per-game ratio)
        # sum(ast) / sum(tov) over L10 prior games avoids inflated values on 0-TOV games
        g["_ast_shift"] = g["ast"].shift(1)
        g["_tov_shift"] = g["tov"].shift(1)
        _ast_sum = g["_ast_shift"].rolling(L10, min_periods=3).sum()
        _tov_sum = g["_tov_shift"].rolling(L10, min_periods=3).sum()
        g["ato_l10"] = (_ast_sum / _tov_sum.replace(0, np.nan)).round(3)
        # Regular-season-only season A:TO (leak-safe season-aggregate scouting
        # value; fallback when the atlas has no ast_to_tov for this player).
        g_reg = g[g["_is_playoff"] == 0]
        _ast_rs, _tov_rs = g_reg["ast"].sum(), g_reg["tov"].sum()
        # Take the last row (as-of the most recent game) as the player's "current" signal
        last = g.iloc[-1].copy()
        rows.append({
            "player_id": int(pid),
            "player_name_gl": last["player_name"],
            "as_of_date": last["game_date"].date().isoformat(),
            "n_games_gl": len(g),
            "ato_season_gl": round(float(_ast_rs / _tov_rs), 3) if _tov_rs > 0 else None,
            "ast_l10": round(float(last["ast_l10"]), 3) if pd.notna(last["ast_l10"]) else None,
            "tov_l10": round(float(last["tov_l10"]), 3) if pd.notna(last["tov_l10"]) else None,
            "ato_l10": round(float(last["ato_l10"]), 3) if pd.notna(last["ato_l10"]) else None,
            "ast_vol_l10": round(float(last["ast_vol_l10"]), 3) if pd.notna(last["ast_vol_l10"]) else None,
        })
    return pd.DataFrame(rows)


# ---------------------------------------------------------------------------
# Assemble the wide playmaking signal table
# ---------------------------------------------------------------------------

def build() -> pd.DataFrame:
    """Build the wide playmaking signal table, one row per player_id.

    Priority: use the most recent season's tracking data; supplement with
    rolling L10 from the current gamelog. Season signals from tracking
    (2024-25 latest) and atlas (2025-26 latest) are joined on player_id.
    """
    # --- Season layers ---
    tracking = _load_tracking_season()
    # Use most recent season per player from tracking
    tracking_latest = (
        tracking.sort_values("season")
        .groupby("player_id", sort=False)
        .last()
        .reset_index()
    )

    atlas = _load_atlas_season()
    # Use 2025-26 from atlas if available, else most recent
    atlas_latest = (
        atlas.sort_values("season")
        .groupby("player_id", sort=False)
        .last()
        .reset_index()
    )

    bbref = _load_bbref_season()
    bbref_latest = (
        bbref.sort_values("season")
        .groupby("player_id", sort=False)
        .last()
        .reset_index()
    )

    hustle = _load_hustle_season()
    hustle_latest = (
        hustle.sort_values("season")
        .groupby("player_id", sort=False)
        .last()
        .reset_index()
    )

    # --- Rolling layer ---
    rolling = _load_gamelog_rolling()

    # --- Merge ---
    # Start with tracking (most data for season sigs)
    df = tracking_latest.copy()

    # Atlas join
    atlas_cols = ["player_id", "ato_season", "atlas_ast_pts_created_pg",
                  "pnr_bh_poss_share", "drive_tov_rate_atlas"]
    df = df.merge(
        atlas_latest[[c for c in atlas_cols if c in atlas_latest.columns]],
        on="player_id", how="outer"
    )
    assert len(df) <= df["player_id"].nunique() * 2, \
        f"Suspicious row count after atlas merge: {len(df)}"

    # BBRef join
    bbref_cols = ["player_id", "ast_pct_bbref", "tov_pct_bbref"]
    df = df.merge(
        bbref_latest[[c for c in bbref_cols if c in bbref_latest.columns]],
        on="player_id", how="left"
    )

    # Hustle join
    hustle_cols = ["player_id", "screen_ast_pg"]
    df = df.merge(
        hustle_latest[[c for c in hustle_cols if c in hustle_latest.columns]],
        on="player_id", how="left"
    )

    # Rolling L10 join
    rolling_cols = ["player_id", "player_name_gl", "as_of_date", "n_games_gl",
                    "ast_l10", "tov_l10", "ato_l10", "ast_vol_l10", "ato_season_gl"]
    df = df.merge(
        rolling[[c for c in rolling_cols if c in rolling.columns]],
        on="player_id", how="outer"
    )

    # Dedupe (outer merges can create multiple rows if player appears in
    # atlas-only and also in rolling-only; collapse to one row per player_id)
    df = df.groupby("player_id", sort=False).last().reset_index()

    # Sanity: row count must be ≈ union of all player_ids
    n_unique = df["player_id"].nunique()
    assert len(df) == n_unique, \
        f"Row count {len(df)} != unique players {n_unique} after dedup"

    # Compute A:TO from tracking ast/tov where atlas ato_season is missing
    if "ast_l10" in df.columns and "tov_l10" in df.columns:
        df["ato_l10"] = df["ato_l10"].where(
            df["ato_l10"].notna(),
            df["ast_l10"] / (df["tov_l10"] + 1e-6)
        )

    # Season A:TO: prefer the direct gamelog regular-season sum (ground truth,
    # current season) over the orphaned atlas ast_to_tov, which is stale/unreliable
    # (e.g. Castle atlas=1.84 vs true 7.38/3.19=2.31) and absent for rookies
    # (e.g. Dylan Harper, who never appears in atlas_player_playmaking_network).
    # Fall back to the atlas only for players with no current-season gamelog.
    if "ato_season_gl" in df.columns:
        if "ato_season" in df.columns:
            df["ato_season"] = df["ato_season_gl"].where(
                df["ato_season_gl"].notna(), df["ato_season"]
            )
        else:
            df["ato_season"] = df["ato_season_gl"]

    # Add a synthetic drive-and-kick rate: drive_and_kick_pg / drives_per_g
    if "drive_and_kick_pg" in df.columns and "drives_per_g" in df.columns:
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            df["drive_kick_rate"] = (
                df["drive_and_kick_pg"] / df["drives_per_g"].replace(0, np.nan)
            ).round(3)

    # Add creation index: ast_pts_created / potential_ast (how often potential -> real ast)
    if "ast_pts_created_pg" in df.columns and "potential_ast_pg" in df.columns:
        df["creation_conversion"] = (
            df["ast_pts_created_pg"] / df["potential_ast_pg"].replace(0, np.nan)
        ).round(3)

    # Round numerical columns
    num_cols = df.select_dtypes("number").columns
    df[num_cols] = df[num_cols].round(4)

    return df


def main():
    os.makedirs(OUT_DIR, exist_ok=True)
    df = build()
    df.to_parquet(OUT, index=False)
    print(f"DONE: playmaking signals -> {OUT}")
    print(f"  rows={len(df)}  distinct players={df['player_id'].nunique()}")
    print()
    # Sample 3 rows
    print("=== 3 sample rows ===")
    sample_cols = ["player_id", "player_name", "season",
                   "ast_pts_created_pg", "potential_ast_pg", "secondary_ast_pg",
                   "ato_season", "pnr_bh_poss_share", "drive_kick_rate",
                   "ast_l10", "tov_l10", "ato_l10"]
    sample_cols = [c for c in sample_cols if c in df.columns]
    sample = df.dropna(subset=["ast_pts_created_pg"]).head(3)
    print(sample[sample_cols].to_string(index=False))
    print()
    # Sanity: top playmakers by ast_pts_created_pg (season-aggregate)
    def _safe(name):
        """ASCII-safe name for Windows cp1252 console."""
        return str(name).encode("ascii", "replace").decode("ascii")

    print("=== Top 10 playmakers by ast_pts_created_pg (season-agg) ===")
    top = df.nlargest(10, "ast_pts_created_pg")
    for r in top.itertuples(index=False):
        name = getattr(r, "player_name", None) or getattr(r, "player_name_gl", "?")
        pts_created = getattr(r, "ast_pts_created_pg", None)
        ato = getattr(r, "ato_season", None)
        pnr = getattr(r, "pnr_bh_poss_share", None)
        print(
            f"  {_safe(name):26s}"
            f"  ast_pts_created={pts_created}"
            f"  ato_season={ato}"
            f"  pnr_bh_share={pnr}"
        )
    print()
    print("=== Top 10 by rolling L10 AST (most recent 10 games, shift(1) leak-free) ===")
    top_l10 = df.nlargest(10, "ast_l10")
    for r in top_l10.itertuples(index=False):
        name = getattr(r, "player_name", None) or getattr(r, "player_name_gl", "?")
        print(
            f"  {_safe(name):26s}"
            f"  ast_l10={getattr(r, 'ast_l10', None)}"
            f"  tov_l10={getattr(r, 'tov_l10', None)}"
            f"  ato_l10={getattr(r, 'ato_l10', None)}"
        )


if __name__ == "__main__":
    main()
