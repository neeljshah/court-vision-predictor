def main():
    os.makedirs(OUT_DIR, exist_ok=True)
    rows = []
    for (entity, domain), defs in DEFS.items():
        for suffix, formula, leak, consumer, ev, gran in defs:
            rows.append({
                "signal_id": f"{entity}.{domain}.{suffix}",
                "entity": entity,
                "domain": domain,
                "granularity": gran,
                "source": "see formula",
                "formula": formula,
                "leak_rule": leak,
                "consumer": consumer,
                "ev_tier": ev,
                "coverage_pct": None,
                "status": "proposed",
            })
    df = pd.DataFrame(rows)
    # Wave-1 shipped domains (builder + fold live, folded into vault notes).
    FOLDED = [
        "player.scoring.", "player.playmaking.", "player.rebounding.", "player.defense.",
        "player.situational.", "player.durability.", "player.form.", "player.correlation.",
        "team.offense.", "team.defense.", "team.lineup_eco.",
        "lineup.fivemix.", "lineup.pair.",
    ]
    for pfx in FOLDED:
        df.loc[df.signal_id.str.startswith(pfx), "status"] = "folded"
    # Deferred: player.movement (CV is thin — intentionally skipped), player.usage, team.context
    for pfx in ["player.movement.", "player.usage.", "team.context."]:
        df.loc[df.signal_id.str.startswith(pfx), "status"] = "deferred"
    pq = os.path.join(OUT_DIR, "signal_registry.parquet")
    df.to_parquet(pq, index=False)

    # human index
    lines = ["# Signal Registry", "",
             f"**{len(df)} signal definitions** across {df.entity.nunique()} entities "
             f"× {df.domain.nunique()} domains. Each expands across entities and the "
             "granularity axes below into the hundreds-per-entity target.", "",
             "Axes: `s`=season `l5/l10/l20`=rolling `opp`=per-opponent `sch`=per-scheme "
             "`q`=per-quarter `sc`=per-shot-clock `scr`=per-game-script `loc`=per-zone", "",
             "EV: **A** moves its consumer · **B** plausible · **C** scouting-only (no overfit risk)", ""]
    for entity in ["player", "team", "lineup"]:
        sub = df[df.entity == entity]
        if sub.empty:
            continue
        lines.append(f"## {entity.title()} — {len(sub)} definitions")
        lines.append("")
        lines.append("| signal_id | gran | consumer | EV | status | formula |")
        lines.append("|---|---|---|---|---|---|")
        for r in sub.itertuples(index=False):
            lines.append(f"| `{r.signal_id}` | {r.granularity} | {r.consumer} | "
                         f"{r.ev_tier} | {r.status} | {r.formula} |")
        lines.append("")
    open(os.path.join(OUT_DIR, "SIGNAL_REGISTRY.md"), "w", encoding="utf-8").write("\n".join(lines))
    by_ev = df.ev_tier.value_counts().to_dict()
    print(f"DONE: {len(df)} signal definitions -> {pq}")
    print(f"  by entity: {df.entity.value_counts().to_dict()}")
    print(f"  by consumer: {df.consumer.value_counts().to_dict()}")
    print(f"  by EV tier: {by_ev}")


if __name__ == "__main__":
    main()
