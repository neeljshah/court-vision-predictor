"""scripts.platformkit.brain_export — Export per-sport intelligence READs as durable
browsable Markdown into the Obsidian brain (_Organized vault).

Purpose: understanding + provenance only, NEVER a number.
Markets efficient; calibration not edge.

Ordering: run AFTER vault_organize_multi.organize_all() (and ideally
brain_digest.build_digests()) so _Organized exists and is fresh.
_Read.md is additive — regenerated each pipeline run.

Public API:
    export_reads(organized_root, sports, write) -> dict

CLI:
    python -m scripts.platformkit.brain_export [--json]
"""
from __future__ import annotations

import argparse
import json
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Dict, List, Optional

# ── Constants ─────────────────────────────────────────────────────────────────
_SPORTS: List[str] = ["nba", "mlb", "soccer", "tennis"]

_SPORT_DIR: Dict[str, str] = {
    "nba": "NBA",
    "mlb": "MLB",
    "soccer": "Soccer",
    "tennis": "Tennis",
}

_HONEST_BANNER = (
    "HONEST: markets efficient; tested signals REJECT; no edge claimed; "
    "calibration NOT edge; surface shows understanding only."
)

_HONEST_HEADER = (
    "> **HONEST** — this file is understanding + provenance only. "
    "No number here is a betting recommendation. "
    "Markets are efficient; all tested signals REJECT."
)


def _frontmatter(sport: str) -> str:
    """Minimal YAML frontmatter for Obsidian tagging."""
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    return "\n".join([
        "---",
        f"tags: [organized, read, {sport}]",
        f"generated: {ts}",
        "edge_claimed: false",
        "---",
        "",
    ])


def export_reads(
    organized_root: Optional[Path] = None,
    sports: Optional[List[str]] = None,
    write: bool = True,
) -> Dict:
    """Export per-sport intelligence READs into vault/_Organized/<SPORT>/_Read.md.

    Parameters
    ----------
    organized_root:
        Path to the _Organized vault directory.  Defaults to
        <repo_root>/vault/_Organized.
    sports:
        Subset of sports to export.  Defaults to all four (_SPORTS).
    write:
        When True (default), write files to disk.

    Returns
    -------
    dict with keys: organized_root, exports, skipped, n_written[, error].
    """
    # Resolve root
    if organized_root is None:
        repo_root = Path(__file__).resolve().parents[2]
        organized_root = repo_root / "vault" / "_Organized"

    organized_root = Path(organized_root)

    if not organized_root.is_dir():
        return {
            "organized_root": str(organized_root),
            "exports": {},
            "n_written": 0,
            "error": "not found",
        }

    target_sports: List[str] = [s.lower() for s in (sports or _SPORTS)]

    # Lazy import — keeps this module light
    from scripts.platformkit.sport_read import build_sport_read, render_markdown  # noqa: PLC0415

    exports: Dict[str, str] = {}
    skipped: List[str] = []
    index_links: List[str] = []

    for sport in target_sports:
        dir_name = _SPORT_DIR.get(sport)
        if dir_name is None:
            skipped.append(sport)
            continue

        sport_dir = organized_root / dir_name
        if not sport_dir.is_dir():
            skipped.append(sport)
            continue

        # Build read (jd=None → no numbers fabricated; use_llm=False → no network)
        read = build_sport_read(sport, jd=None, root=organized_root, use_llm=False)

        md_body = render_markdown(read)

        # Compose full file: frontmatter + honest header + rendered body
        content = (
            _frontmatter(sport)
            + _HONEST_HEADER
            + "\n\n"
            + md_body
        )

        dest = sport_dir / "_Read.md"

        if write:
            dest.write_text(content, encoding="utf-8")

        exports[sport] = str(dest)
        rel = f"../{dir_name}/_Read.md"
        index_links.append(
            f"- [{dir_name} Intelligence Read]({rel}) — {_HONEST_BANNER}"
        )

    # Write index
    index_dir = organized_root / "_Index"
    index_path = index_dir / "_Reads.md"
    ts_human = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    index_content_lines = [
        "# Intelligence Reads — Index",
        "",
        f"> Generated: {ts_human}",
        f"> {_HONEST_BANNER}",
        "",
        "## Per-Sport Intelligence Read Files",
        "",
    ] + index_links + [
        "",
        "---",
        "_This index is auto-generated by `scripts.platformkit.brain_export`. "
        "Regenerate with `python -m scripts.platformkit.brain_export`._",
    ]

    index_content = "\n".join(index_content_lines) + "\n"

    if write:
        index_dir.mkdir(parents=True, exist_ok=True)
        index_path.write_text(index_content, encoding="utf-8")

    return {
        "organized_root": str(organized_root),
        "exports": exports,
        "skipped": skipped,
        "n_written": len(exports),
        "index": str(index_path),
    }


def _cli(argv: Optional[List[str]] = None) -> int:
    ap = argparse.ArgumentParser(
        description=(
            "brain_export: export per-sport intelligence READs into vault/_Organized. "
            "No numbers; no edge claims."
        )
    )
    ap.add_argument("--json", action="store_true", help="Print JSON report.")
    ap.add_argument(
        "--sports",
        nargs="*",
        default=None,
        help="Sports to export (default: all four).",
    )
    ap.add_argument(
        "--root",
        default=None,
        help="Override path to _Organized vault directory.",
    )
    a = ap.parse_args(argv)

    root = Path(a.root) if a.root else None
    report = export_reads(organized_root=root, sports=a.sports or None, write=True)

    if a.json:
        print(json.dumps(report, indent=2))
    else:
        print(f"organized_root : {report['organized_root']}")
        print(f"n_written      : {report.get('n_written', 0)}")
        print(f"skipped        : {report.get('skipped', [])}")
        for sport, path in report.get("exports", {}).items():
            print(f"  {sport:8s} -> {path}")
        if "index" in report:
            print(f"index          : {report['index']}")
        if "error" in report:
            print(f"ERROR          : {report['error']}", file=sys.stderr)
            return 1
    return 0


if __name__ == "__main__":
    sys.exit(_cli())
