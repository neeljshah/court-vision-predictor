#!/usr/bin/env python
"""Generate publishable artifacts under results/ from current model registry.

Honest by design: produces what is measurable today, and reports clearly what is
gated on downstream data (CV ingest + paper-trading gate). Matches the
MEASURED / PROJECTED split in the project README.

Run:
    python scripts/generate_results.py
"""

from __future__ import annotations

import csv
import json
from pathlib import Path
from typing import Any

ROOT = Path(__file__).resolve().parents[1]
REGISTRY = ROOT / "data" / "models" / "model_registry.json"
GAME_METRICS = ROOT / "data" / "models" / "game_models_metrics.json"
RESULTS = ROOT / "results"

PROP_TARGETS = {
    "props_pts": 0.45,
    "props_reb": 0.40,
    "props_ast": 0.40,
    "props_fg3m": 0.32,
    "props_tov": 0.25,
    "props_blk": 0.22,
    "props_stl": 0.22,
}


def _load_json(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {}
    with path.open() as f:
        return json.load(f)


def write_holdout_csv(registry: dict[str, Any]) -> Path:
    out = RESULTS / "holdout_metrics.csv"
    with out.open("w", newline="") as f:
        w = csv.writer(f)
        w.writerow(["model", "holdout_r2", "holdout_mae", "train_r2", "train_mae",
                    "train_n", "holdout_n", "target_r2", "gap_to_target"])
        for name, m in registry.items():
            tgt = PROP_TARGETS.get(name)
            gap = (tgt - m["holdout_r2"]) if tgt is not None else ""
            w.writerow([
                name,
                m.get("holdout_r2", ""),
                m.get("holdout_mae", ""),
                m.get("train_r2", ""),
                m.get("train_mae", ""),
                m.get("train_n", ""),
                m.get("holdout_n", ""),
                tgt if tgt is not None else "",
                gap,
            ])
    return out


def write_holdout_chart(registry: dict[str, Any]) -> Path | None:
    try:
        import matplotlib

        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception:
        return None

    names = list(registry.keys())
    holdout = [registry[n]["holdout_r2"] for n in names]
    train = [registry[n]["train_r2"] for n in names]
    targets = [PROP_TARGETS.get(n, 0.0) for n in names]

    x = range(len(names))
    fig, ax = plt.subplots(figsize=(10, 4.5))
    width = 0.32
    ax.bar([i - width for i in x], train, width=width, label="Train R²", color="#888")
    ax.bar(list(x), holdout, width=width, label="Holdout R²", color="#1f77b4")
    ax.bar([i + width for i in x], targets, width=width, label="Target",
           color="#2ca02c", alpha=0.4)
    ax.set_xticks(list(x))
    ax.set_xticklabels([n.replace("props_", "") for n in names])
    ax.set_ylabel("R²")
    ax.set_title("Prop models — walk-forward holdout R² (API features only)")
    ax.legend()
    ax.grid(axis="y", alpha=0.3)
    fig.tight_layout()
    out = RESULTS / "holdout_metrics.png"
    fig.savefig(out, dpi=120)
    plt.close(fig)
    return out


def write_status_md(registry: dict[str, Any], game_metrics: dict[str, Any],
                    produced: list[Path]) -> Path:
    gated = [
        ("reliability_*.png", "per-prop reliability diagram",
         "holdout predictions parquet — requires backtester checkpoint dump"),
        ("clv_by_market.png / clv_by_time.png / clv_cumulative.png",
         "CLV plots vs Pinnacle close",
         "paper-trading gate (≥50 settled bets logged to data/clv_log.json)"),
        ("ece_by_model.csv", "calibration ECE/MCE per model",
         "isotonic calibration evaluation on holdout — runs after 80-game CV ingest"),
        ("clv_picks.csv", "settled pick log with realized CLV",
         "paper-trading gate (≥50 settled bets)"),
    ]
    lines = ["# Results — generated artifacts", ""]
    lines.append(f"_Generated by `scripts/generate_results.py`._")
    lines.append("")
    lines.append("## Produced")
    if produced:
        for p in produced:
            lines.append(f"- `{p.relative_to(ROOT).as_posix()}`")
    else:
        lines.append("_(nothing produced — registry missing)_")
    lines.append("")
    lines.append("## Holdout metrics (current)")
    lines.append("")
    lines.append("| Model | Holdout R² | MAE | Target | Gap |")
    lines.append("|-------|------------|-----|--------|-----|")
    for name, m in registry.items():
        tgt = PROP_TARGETS.get(name)
        short = name.replace("props_", "")
        if tgt is not None:
            gap = tgt - m["holdout_r2"]
            lines.append(
                f"| {short} | {m['holdout_r2']:.3f} | {m['holdout_mae']:.2f} | "
                f"{tgt:.2f} | {gap:+.3f} |"
            )
        else:
            lines.append(
                f"| {short} | {m['holdout_r2']:.3f} | {m['holdout_mae']:.2f} | "
                f"— | — |"
            )
    if game_metrics:
        # `pace` is excluded — its target is currently a linear combination of
        # input features (synthetic-data placeholder), so the reported R² is
        # not meaningful until the real-PBP pace target lands.
        publishable = {k: v for k, v in game_metrics.items() if k != "pace"}
        if publishable:
            lines += ["", "## Game-level models (synthetic-data training)", "",
                      "_These models train on a synthetic distribution while the "
                      "real game-features parquet is being assembled. Numbers are "
                      "implementation-validity checks, not predictive claims._",
                      "",
                      "| Model | Metric | Value | N |",
                      "|-------|--------|-------|---|"]
            for name, m in publishable.items():
                if "r2" in m:
                    lines.append(f"| {name} | R² | {m['r2']:.3f} | {m.get('n','')} |")
                if "brier" in m:
                    lines.append(
                        f"| {name} | Brier | {m['brier']:.3f} | {m.get('n','')} |"
                    )
    lines += ["", "## Gated artifacts — produced when blocker clears", ""]
    for artifact, desc, gate in gated:
        lines.append(f"- **{artifact}** — {desc}. _Gated on:_ {gate}.")
    lines += ["",
              "See [README §Results](../README.md#results) for the MEASURED vs "
              "PROJECTED split."]
    out = RESULTS / "STATUS.md"
    out.write_text("\n".join(lines), encoding="utf-8")
    return out


def main() -> int:
    if not REGISTRY.exists():
        print(f"[ERROR] {REGISTRY.relative_to(ROOT)} missing — train props first.")
        return 1

    RESULTS.mkdir(exist_ok=True)
    registry = _load_json(REGISTRY)
    game_metrics = _load_json(GAME_METRICS)

    produced: list[Path] = []
    produced.append(write_holdout_csv(registry))
    chart = write_holdout_chart(registry)
    if chart is not None:
        produced.append(chart)
    else:
        print("[warn] matplotlib unavailable — skipped holdout_metrics.png")
    produced.append(write_status_md(registry, game_metrics, produced))

    print(f"[ok] wrote {len(produced)} artifacts to {RESULTS.relative_to(ROOT)}/")
    for p in produced:
        print(f"      {p.relative_to(ROOT).as_posix()}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
