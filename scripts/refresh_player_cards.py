# Eligibility
# ══════════════════════════════════════════════════════════════════════════

def get_new_eligible_players(data: dict, existing_ids: set[int]) -> list[int]:
    """
    Players with >=3 CV games AND >=5 prop (player_pf) games AND not in existing 124.
    """
    cv_gpg = data["cv_gpg"]
    cv_eligible = set(cv_gpg[cv_gpg["n_cv_games"] >= 3]["player_id"].astype(int))

    pf = data["pf"]
    pf_gpg = pf.groupby("player_id").size()
    prop_eligible = set(pf_gpg[pf_gpg >= 5].index.astype(int))

    new_ids = (cv_eligible & prop_eligible) - existing_ids
    return sorted(new_ids)


