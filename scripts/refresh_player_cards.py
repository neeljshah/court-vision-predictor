# Marker upsert
# ══════════════════════════════════════════════════════════════════════════

def _make_section_block(marker: str, body: str) -> str:
    return f"<!-- BEGIN: {marker} -->\n{body}\n<!-- END: {marker} -->\n"


def _upsert_section(content: str, marker: str, body: str) -> tuple[str, bool]:
    """
    Idempotent upsert of a BEGIN/END marker block.
    Returns (new_content, was_changed).
    """
    begin_pat = re.compile(
        rf"<!-- BEGIN: {re.escape(marker)} -->.*?<!-- END: {re.escape(marker)} -->",
        re.DOTALL,
    )
    new_block = _make_section_block(marker, body)

    if begin_pat.search(content):
        new_content = begin_pat.sub(new_block.rstrip("\n"), content)
        return new_content, (new_content != content)
    else:
        # Insert before footer
        footer_idx = content.rfind("\n---\n")
        if footer_idx != -1:
            new_content = content[:footer_idx] + "\n\n" + new_block + content[footer_idx:]
        else:
            new_content = content.rstrip("\n") + "\n\n" + new_block
        return new_content, True


def refresh_existing_card(
    card_path: Path,
    player_id: int,
    data: dict,
) -> tuple[bool, bool]:
    """
    Append/upsert 5 sections into an existing card.
    Returns (modified, content_loss_risk).
    """
    original = card_path.read_text(encoding="utf-8")
    original_lines = len(original.splitlines())
    content = original

    section_builders = {
        "opponent_context_v1": build_opponent_context,
        "development_trend_v1": build_development_trend,
        "archetype_outlier_v1": build_archetype_outlier,
        "per_player_calibration_v1": build_calibration,
        "garbage_time_exposure_v1": build_garbage_time,
    }

    any_changed = False
    for marker in SECTIONS:
        body = section_builders[marker](player_id, data)
        content, changed = _upsert_section(content, marker, body)
        if changed:
            any_changed = True

    if any_changed:
        card_path.write_text(content, encoding="utf-8")

    # Content-loss check: original lines should be present
    final_lines = len(content.splitlines())
    content_loss = (final_lines < original_lines)

    return any_changed, content_loss


