def _get_player_team(player_id: int, data: dict) -> str:
    """Best-effort team abbreviation lookup."""
    # INT56 (most recent games)
    int56 = data["int56"]
    rows = int56[int56["player_id"] == player_id].sort_values("game_date", ascending=False)
    if not rows.empty and pd.notna(rows.iloc[0].get("team")):
        return str(rows.iloc[0]["team"]).upper()
    # roster dict
    roster = data["roster"]
    if player_id in roster and "team" in roster[player_id]:
        return str(roster[player_id]["team"]).upper()
    # player_pf
    pf = data["pf"]
    pf_rows = pf[pf["player_id"] == player_id].sort_values("game_date", ascending=False)
    if not pf_rows.empty and "team_abbreviation" in pf_rows.columns:
        return str(pf_rows.iloc[0]["team_abbreviation"]).upper()
    # player_positions
    pp = data["pp"]
    pp_rows = pp[pp["player_id"] == player_id]
    if not pp_rows.empty:
        return "UNK"
    return "UNK"


def _get_player_game_dates(player_id: int, data: dict) -> list[str]:
    """Return sorted list of game_dates played (from player_pf)."""
    pf = data["pf"]
    rows = pf[pf["player_id"] == player_id].sort_values("game_date")
    return rows["game_date"].dt.strftime("%Y-%m-%d").tolist()


def build_opponent_context(player_id: int, data: dict) -> str:
    """Section 1: Opponent defensive context faced (C3+C4) — last 5 games."""
    pf = data["pf"]
    c3 = data["c3"]
    c4 = data["c4"]

    player_games = pf[pf["player_id"] == player_id].sort_values("game_date", ascending=False)
    if player_games.empty:
        return _unavailable("no game history")

    # Get last 5 game opponent teams
    # We need to know the opponent: player_pf has team_abbreviation (player's team).
    # The opponent is the "other" team in that game. We'll approximate:
    # Look at opp_defensive_intensity by date-proximity to the player's game dates.
    # C3/C4 is keyed by (team_id, game_date) where team_id is the DEFENDING team.
    # We want: for each of player's recent games, what was the opp def intensity?
    # Since we don't have a direct player->opponent mapping here, we use:
    # - player's team from player_pf, then find games in C3 where a *different* team
    #   matches that game_date.
    # Simpler: use the most recent C3/C4 snapshot across all opponents they faced.
    # For an approximation: average C3/C4 for the player's team games.

    l5 = player_games.head(5)
    team = None
    if "team_abbreviation" in l5.columns:
        team = l5["team_abbreviation"].mode().iloc[0] if not l5["team_abbreviation"].mode().empty else None

    if team is None:
        return _unavailable("team unknown")

    # Get C3/C4 for team's recent games as proxy for what they faced
    # (C3/C4 is indexed by team_id = defending team; player's OPP is the defending team)
    # We can't directly invert without game-level matchup data, so we use league mean
    # from C3 data for comparable intensity context.
    # Best available: filter C3 where team_id != player's team and date in range
    l5_dates = l5["game_date"].tolist()
    min_date = min(l5_dates) - pd.Timedelta(days=3)
    max_date = max(l5_dates) + pd.Timedelta(days=1)

    c3_window = c3[(c3["game_date"] >= min_date) & (c3["game_date"] <= max_date)]
    c4_window = c4[(c4["game_date"] >= min_date) & (c4["game_date"] <= max_date)]

    if c3_window.empty and c4_window.empty:
        return _unavailable("no C3/C4 data in window")

    def _fmt_z(val: float | None, name: str) -> str:
        if val is None or (isinstance(val, float) and np.isnan(val)):
            return f"{name}: _n/a_"
        sign = "+" if val > 0 else ""
        return f"{name}: {sign}{val:.2f}σ"

    lines = ["### Opponent Context (last 5 games window)"]
    lines.append("")

    if not c3_window.empty:
        avg_def_intensity = c3_window["opp_defensive_intensity_z"].mean()
        avg_defender_dist = c3_window["opp_avg_defender_distance_imposed_z"].mean()
        avg_contested = c3_window["opp_contested_shot_rate_imposed_z"].mean()
        density = c3_window["data_density"].mode().iloc[0] if not c3_window["data_density"].mode().empty else "?"
        lines.append(f"- {_fmt_z(avg_def_intensity, 'Avg opp def intensity (z)')}")
        lines.append(f"- {_fmt_z(avg_defender_dist, 'Avg opp defender distance imposed (z)')}")
        lines.append(f"- {_fmt_z(avg_contested, 'Avg opp contested shot rate imposed (z)')}")
        lines.append(f"- Data density: {density}")
    else:
        lines.append(f"- C3 defensive intensity: {_unavailable('no window data')}")

    if not c4_window.empty:
        avg_paint_pct = c4_window["opp_paint_pct_allowed_z"].mean()
        avg_3pt = c4_window["opp_3pt_pct_allowed_z"].mean()
        lines.append(f"- {_fmt_z(avg_paint_pct, 'Avg opp paint pct allowed (z)')}")
        lines.append(f"- {_fmt_z(avg_3pt, 'Avg opp 3PT pct allowed (z)')}")
    else:
        lines.append(f"- C4 paint allowance: {_unavailable('no window data')}")

    lines.append(f"- Window: {min_date.date()} – {max_date.date()}")
    return "\n".join(lines)


def build_development_trend(player_id: int, data: dict) -> str:
    """Section 2: Development trend from INT-67."""
    int67 = data["int67"]
    rows = int67[int67["player_id"] == player_id].sort_values("game_date", ascending=False)

    if rows.empty:
        return _unavailable("not in INT-67 development atlas")

    latest = rows.iloc[0]
    dev_score = latest.get("dev_score")
    top_trending = latest.get("top_trending")
    any_regime = latest.get("any_regime_change")
    game_date = latest.get("game_date")
    n_window = latest.get("n_window", "?")

    lines = ["### Development Trend (INT-67)"]
    lines.append("")

    if dev_score is not None and not (isinstance(dev_score, float) and np.isnan(dev_score)):
        lines.append(f"- **Dev score:** {dev_score:.2f} (n_window={n_window})")
    else:
        lines.append(f"- Dev score: {_unavailable()}")

    if top_trending is not None and str(top_trending) not in ("", "nan"):
        # top_trending may be a JSON string like [{"feature": "x", "slope_z": 1.2, "direction": "up"}, ...]
        try:
            tt_parsed = json.loads(str(top_trending)) if isinstance(top_trending, str) else top_trending
            if isinstance(tt_parsed, list) and tt_parsed:
                top_feat = tt_parsed[0]
                feat_name = top_feat.get("feature", "?")
                direction = top_feat.get("direction", "?")
                slope_z = top_feat.get("slope_z", None)
                sz_str = f" (z={slope_z:+.2f})" if slope_z is not None else ""
                lines.append(f"- **Top trending feature:** {feat_name} {direction}{sz_str}")
                if len(tt_parsed) > 1:
                    others = [f"{t.get('feature','?')} {t.get('direction','?')}" for t in tt_parsed[1:3]]
                    lines.append(f"- Other trends: {', '.join(others)}")
            elif isinstance(tt_parsed, str):
                lines.append(f"- **Top trending feature:** {tt_parsed}")
            else:
                lines.append(f"- **Top trending feature:** {top_trending}")
        except Exception:
            lines.append(f"- **Top trending feature:** {top_trending}")
    else:
        lines.append("- Top trending feature: _none flagged_")

    regime_str = "yes — regime change detected" if any_regime else "no active regime change"
    lines.append(f"- Regime change: {regime_str}")

    if game_date is not None:
        dt_str = pd.to_datetime(game_date).strftime("%Y-%m-%d") if not isinstance(game_date, str) else game_date
        lines.append(f"- As of: {dt_str}")

    return "\n".join(lines)


def build_archetype_outlier(player_id: int, data: dict) -> str:
    """Section 3: Archetype outlier state from INT-54."""
    int54 = data["int54"]
    rows = int54[int54["player_id"] == player_id].sort_values("game_date", ascending=False)

    if rows.empty:
        return _unavailable("not in INT-54 outlier signals")

    latest = rows.iloc[0]
    outlier_z = latest.get("outlier_z")
    direction_top3 = latest.get("direction_top3")
    flag_strong = latest.get("flag_strong_outlier")
    game_date = latest.get("game_date")
    n_hist = latest.get("n_hist_games")

    lines = ["### Archetype Outlier State (INT-54)"]
    lines.append("")

    if outlier_z is not None and not (isinstance(outlier_z, float) and np.isnan(outlier_z)):
        sign = "+" if outlier_z > 0 else ""
        lines.append(f"- **Outlier z-score (recent):** {sign}{outlier_z:.3f}")
    else:
        lines.append(f"- Outlier z-score: {_unavailable()}")

    flag_str = " **(STRONG OUTLIER)**" if flag_strong else ""
    lines.append(f"- Strong outlier flag: {'yes' if flag_strong else 'no'}{flag_str}")

    if direction_top3 is not None and str(direction_top3) not in ("", "nan"):
        try:
            if isinstance(direction_top3, str):
                d = json.loads(direction_top3)
            else:
                d = direction_top3
            top_features = [f"{k}: {'+' if v > 0 else ''}{v:.2f}" for k, v in list(d.items())[:3]]
            lines.append(f"- Top drivers: {', '.join(top_features)}")
        except Exception:
            lines.append(f"- Direction: {direction_top3}")
    else:
        lines.append("- Top drivers: _none_")

    if n_hist is not None:
        lines.append(f"- Historical games in window: {int(n_hist)}")

    if game_date is not None:
        dt_str = pd.to_datetime(game_date).strftime("%Y-%m-%d") if not isinstance(game_date, str) else str(game_date)
        lines.append(f"- As of: {dt_str}")

    return "\n".join(lines)


def build_calibration(player_id: int, data: dict) -> str:
    """Section 4: Per-player calibration bias from INT-69."""
    int69 = data["int69"]
    rows = int69[int69["player_id"] == player_id]

    if rows.empty:
        return _unavailable("not in INT-69 calibration")

    # Latest entry per stat
    latest_per_stat = (
        rows.sort_values("asof_date", ascending=False)
        .groupby("stat")
        .first()
        .reset_index()
    )

    lines = ["### Per-Player Calibration (INT-69)"]
    lines.append("")

    stat_order = ["pts", "reb", "ast", "stl", "blk", "tov", "fg3m"]
    rows_shown = []
    for stat in stat_order:
        r = latest_per_stat[latest_per_stat["stat"] == stat]
        if r.empty:
            continue
        r = r.iloc[0]
        bias = r.get("bias_l20")
        bias_z = r.get("bias_z_l20")
        n = r.get("n_prior_games")

        if bias is not None and not (isinstance(bias, float) and np.isnan(bias)):
            sign = "+" if bias > 0 else ""
            z_str = ""
            if bias_z is not None and not (isinstance(bias_z, float) and np.isnan(bias_z)):
                z_str = f" (z={bias_z:+.2f})"
            rows_shown.append(f"- **{stat.upper()}:** bias_l20={sign}{bias:.3f}{z_str}, n={int(n) if pd.notna(n) else '?'}")

    if rows_shown:
        lines.extend(rows_shown)
    else:
        lines.append(f"- {_unavailable('no stat rows available')}")

    return "\n".join(lines)


def build_garbage_time(player_id: int, data: dict) -> str:
    """Section 5: Garbage-time exposure from INT-56, last 5 games."""
    int56 = data["int56"]
    rows = int56[int56["player_id"] == player_id].sort_values("game_date", ascending=False)

    if rows.empty:
        return _unavailable("not in INT-56 garbage-time atlas")

    l5 = rows.head(5)
    avg_gt_pct = l5["pct_minutes_in_gt"].mean()
    avg_gt_entries = l5["gt_entry_count"].mean()
    starter_flag = l5["primary_starter_flag"].mode().iloc[0] if not l5["primary_starter_flag"].mode().empty else False
    n_games = len(l5)

    lines = ["### Garbage-Time Exposure (INT-56)"]
    lines.append("")
    lines.append(f"- **Avg GT min % (l{n_games}):** {avg_gt_pct:.1%}")
    lines.append(f"- **Avg GT entries per game (l{n_games}):** {avg_gt_entries:.1f}")
    lines.append(f"- **Primary starter:** {'yes' if starter_flag else 'no'}")

    # Flag if heavy GT exposure
    if avg_gt_pct > 0.20:
        lines.append("- _Note: high GT exposure — stats may be inflated_")
    elif avg_gt_pct == 0:
        lines.append("- _No garbage-time minutes in window_")

    if n_games < 5:
        lines.append(f"- _Only {n_games} game(s) available in window_")

    return "\n".join(lines)


