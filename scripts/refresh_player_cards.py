def _slug(name: str) -> str:
    """Convert display name to file slug."""
    return re.sub(r"[^a-z0-9]+", "_", name.lower()).strip("_")


def _get_player_display_name(player_id: int, data: dict) -> str:
    """Resolve display name from best available source."""
    # player_positions is most reliable
    pp = data["pp"]
    r = pp[pp["player_id"] == player_id]
    if not r.empty and "display_name" in r.columns:
        return str(r.iloc[0]["display_name"])
    # INT-56 has partial names (last name only typically)
    int56 = data["int56"]
    r56 = int56[int56["player_id"] == player_id]
    if not r56.empty and "player_name" in r56.columns:
        return str(r56.iloc[0]["player_name"])
    # roster
    roster = data["roster"]
    if player_id in roster:
        for name_key in ["display_first_last", "full_name", "player_name"]:
            if name_key in roster[player_id]:
                return roster[player_id][name_key]
    return f"Player_{player_id}"


def build_new_card(player_id: int, data: dict) -> str:
    """Generate a new player card (standard frontmatter + base + 5 sections)."""
    name = _get_player_display_name(player_id, data)
    team = _get_player_team(player_id, data)
    archetype = "Unknown"

    fp = data["fp"]
    if player_id in fp.index:
        archetype = str(fp.loc[player_id, "archetype_name"])
        n_cv = int(fp.loc[player_id, "n_cv_games"])
    else:
        cv_gpg = data["cv_gpg"]
        n_row = cv_gpg[cv_gpg["player_id"] == player_id]
        n_cv = int(n_row["n_cv_games"].iloc[0]) if not n_row.empty else 0

    # Player pf game history
    pf = data["pf"]
    player_games = pf[pf["player_id"] == player_id].sort_values("game_date")
    n_prop_games = len(player_games)

    # Game IDs by season
    game_ids_by_season: dict[str, list[str]] = {}
    if "game_id" in player_games.columns and "game_date" in player_games.columns:
        for _, row in player_games.iterrows():
            gdate = pd.to_datetime(row["game_date"])
            season = "2025-26" if gdate.year >= 2025 and gdate.month >= 10 else (
                "2024-25" if gdate.year >= 2024 or (gdate.year == 2025 and gdate.month < 10) else "prior"
            )
            game_ids_by_season.setdefault(season, []).append(str(row["game_id"]))

    # Frontmatter
    lines = [
        "---",
        f"player_id: {player_id}",
        f"name: {name}",
        f"team: {team}",
        f"archetype: {archetype}",
        f"n_cv_games: {n_cv}",
        f"n_seasons_tracked: {max(1, len(game_ids_by_season))}",
        f"last_updated: {TODAY}",
        "---",
        "",
        f"# {name} ({team}) — CV Intelligence Card",
        "",
        f"## Behavioral Fingerprint ({n_cv} CV games tracked)",
        "",
        f"**Archetype:** {archetype}",
        "",
    ]

    # Base CV data from fingerprints
    if player_id in fp.index:
        fp_row = fp.loc[player_id]
        dist = fp_row.get("dist_from_centroid")
        if dist is not None and not (isinstance(dist, float) and np.isnan(dist)):
            lines.append(f"- **Dist from archetype centroid:** {dist:.2f}σ")

        # Shot zones
        for feat, label in [
            ("shot_zone_paint_pct", "Paint shots"),
            ("shot_zone_mid_range_pct", "Mid-range shots"),
            ("shot_zone_3pt_pct", "3PT shots"),
        ]:
            val = fp_row.get(feat)
            if val is not None and not (isinstance(val, float) and np.isnan(val)):
                lines.append(f"- **{label}:** {val:.1%}")

        # Key metrics
        for feat, label, unit in [
            ("touches_per_game", "Touches per game", ""),
            ("avg_shot_distance", "Avg shot distance", " ft"),
            ("catch_and_shoot_rate", "Catch-and-shoot rate", ""),
        ]:
            val = fp_row.get(feat)
            if val is not None and not (isinstance(val, float) and np.isnan(val)):
                fmt = f"{val:.1%}" if unit == "" and "rate" in feat else f"{val:.1f}{unit}"
                lines.append(f"- **{label}:** {fmt}")
        lines.append("")

    # Per-game breakdown
    lines.append("## Per-Game Breakdown")
    lines.append("")
    lines.append("*Game IDs tracked (player_pf):*")
    for season, gids in sorted(game_ids_by_season.items()):
        gids_str = ", ".join(f"`{g}`" for g in gids[:15])
        if len(gids) > 15:
            gids_str += f" (+{len(gids)-15} more)"
        lines.append(f"- **{season}:** {gids_str}")
    if not game_ids_by_season:
        lines.append("- _No game history in player_pf_")
    lines.append("")

    lines.append("---")
    lines.append(f"*Generated by `scripts/refresh_player_cards.py` on {TODAY} | Source: cv_features + INT-2 + INT-54/56/67/69*")
    lines.append("*[[_Player_Index]] | [[Player_Atlas]]*")

    card_text = "\n".join(lines)

    # Now append the 5 sections
    section_builders = {
        "opponent_context_v1": build_opponent_context,
        "development_trend_v1": build_development_trend,
        "archetype_outlier_v1": build_archetype_outlier,
        "per_player_calibration_v1": build_calibration,
        "garbage_time_exposure_v1": build_garbage_time,
    }
    for marker in SECTIONS:
        body = section_builders[marker](player_id, data)
        card_text, _ = _upsert_section(card_text, marker, body)

    return card_text


