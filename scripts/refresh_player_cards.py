# Index update
# ══════════════════════════════════════════════════════════════════════════

def update_index(new_entries: list[tuple[int, str, str, str]]) -> None:
    """
    Append ### Newly Added block to _Player_Index.md.
    new_entries: list of (player_id, display_name, team, archetype)
    """
    if not new_entries:
        return

    index_text = INDEX_PATH.read_text(encoding="utf-8")

    # Remove any existing ### Newly Added block (idempotent)
    index_text = re.sub(
        r"\n### Newly Added.*$",
        "",
        index_text,
        flags=re.DOTALL,
    )

    lines = ["\n\n### Newly Added"]
    for pid, name, team, archetype in sorted(new_entries, key=lambda x: x[1]):
        slug = _slug(name)
        lines.append(f"- [{name}](Players/{pid}_{slug}.md) ({team}) — {archetype}")

    index_text = index_text.rstrip() + "\n".join(lines) + "\n"
    INDEX_PATH.write_text(index_text, encoding="utf-8")


