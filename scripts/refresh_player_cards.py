def load_all_data() -> dict[str, Any]:
    """Load all 6 source parquets + supporting data into memory."""
    data: dict[str, Any] = {}

    # C3: opp defensive intensity (team-game level)
    data["c3"] = pd.read_parquet(INTEL_DIR / "opp_defensive_intensity.parquet")
    data["c3"]["game_date"] = pd.to_datetime(data["c3"]["game_date"])

    # C4: opp paint allowance (team-game level)
    data["c4"] = pd.read_parquet(INTEL_DIR / "opp_paint_allowance.parquet")
    data["c4"]["game_date"] = pd.to_datetime(data["c4"]["game_date"])

    # INT-54: archetype outlier signals (player-game level)
    data["int54"] = pd.read_parquet(INTEL_DIR / "archetype_outlier_signals.parquet")

    # INT-56: garbage time player aggregates (player-game level)
    data["int56"] = pd.read_parquet(INTEL_DIR / "garbage_time_player_aggregates.parquet")
    data["int56"]["player_id"] = data["int56"]["player_id"].astype("Int64")

    # INT-67: player development v2 (player-game level)
    data["int67"] = pd.read_parquet(INTEL_DIR / "player_development_v2.parquet")

    # INT-69: per-player calibration (player-stat-date level)
    data["int69"] = pd.read_parquet(INTEL_DIR / "per_player_calibration.parquet")
    data["int69"]["asof_date"] = pd.to_datetime(data["int69"]["asof_date"])

    # Supporting: player_pf for team + game history
    data["pf"] = pd.read_parquet(PLAYER_PF_PATH)
    data["pf"]["game_date"] = pd.to_datetime(data["pf"]["game_date"])

    # Supporting: fingerprints
    data["fp"] = pd.read_parquet(FINGERPRINTS_PATH)

    # Supporting: player_positions for display_name, team
    data["pp"] = pd.read_parquet(PLAYER_POSITIONS_PATH)

    # Supporting: rosters for team abbreviations
    roster: dict[int, dict] = {}
    for path in [ROSTER_PATH_2425, ROSTER_PATH_2526]:
        if path.exists():
            with open(path) as f:
                raw = json.load(f)
            for v in raw.values():
                if isinstance(v, dict) and "player_id" in v and "team" in v:
                    pid = int(v["player_id"])
                    roster[pid] = v
    data["roster"] = roster

    # Supporting: CV games per player from DB
    conn = sqlite3.connect(DB_PATH)
    try:
        cv_gpg = pd.read_sql(
            "SELECT player_id, COUNT(DISTINCT game_id) as n_cv_games FROM cv_features GROUP BY player_id",
            conn,
        )
        cv_gpg["player_id"] = cv_gpg["player_id"].astype(int)
        data["cv_gpg"] = cv_gpg
    finally:
        conn.close()

    return data


