# Main
# ══════════════════════════════════════════════════════════════════════════

def main() -> None:
    print(f"[refresh_player_cards] Starting — {TODAY}")
    print("Loading data...")
    data = load_all_data()

    # ── Phase 1: refresh existing 124 cards ───────────────────────────────
    existing_files = sorted(PLAYERS_DIR.glob("*.md"))
    existing_map: dict[int, Path] = {}
    for f in existing_files:
        try:
            pid = int(f.stem.split("_")[0])
            existing_map[pid] = f
        except ValueError:
            continue

    print(f"Found {len(existing_map)} existing player cards.")

    refreshed = 0
    skipped = 0
    content_loss_warnings = 0

    for player_id, card_path in existing_map.items():
        try:
            modified, content_loss = refresh_existing_card(card_path, player_id, data)
            if modified:
                refreshed += 1
            else:
                skipped += 1
            if content_loss:
                content_loss_warnings += 1
                print(f"  WARNING: possible content loss in {card_path.name}")
        except Exception as e:
            print(f"  ERROR refreshing {card_path.name}: {e}")
            skipped += 1

    print(f"Refresh pass: refreshed={refreshed}, unchanged={skipped}, content_loss_warnings={content_loss_warnings}")

    # ── Phase 2: new cards ────────────────────────────────────────────────
    new_eligible = get_new_eligible_players(data, set(existing_map.keys()))
    print(f"New eligible players: {len(new_eligible)} — {new_eligible}")

    new_entries_for_index: list[tuple[int, str, str, str]] = []
    new_created = 0
    data_sparse = 0

    for player_id in new_eligible:
        try:
            name = _get_player_display_name(player_id, data)
            team = _get_player_team(player_id, data)
            slug = _slug(name)
            fname = f"{player_id}_{slug}.md"
            out_path = PLAYERS_DIR / fname

            card_text = build_new_card(player_id, data)

            # Check data sparseness
            fp = data["fp"]
            int54 = data["int54"]
            int67 = data["int67"]
            int69 = data["int69"]

            has_int54 = player_id in int54["player_id"].values
            has_int67 = player_id in int67["player_id"].values
            has_int69 = player_id in int69["player_id"].values
            is_sparse = not (has_int54 or has_int67 or has_int69)
            if is_sparse:
                data_sparse += 1

            out_path.write_text(card_text, encoding="utf-8")
            new_created += 1

            # Fingerprint archetype
            archetype = "Unknown"
            if player_id in fp.index:
                archetype = str(fp.loc[player_id, "archetype_name"])

            new_entries_for_index.append((player_id, name, team, archetype))
            print(f"  Created: {fname}")

        except Exception as e:
            print(f"  ERROR creating card for {player_id}: {e}")

    # ── Phase 3: update index ─────────────────────────────────────────────
    update_index(new_entries_for_index)

    # ── Validation checks ─────────────────────────────────────────────────
    all_cards = list(PLAYERS_DIR.glob("*.md"))
    total_cards = len(all_cards)

    # Spot-check: verify all 5 markers present in each refreshed card
    cards_missing_markers = 0
    for card_path in all_cards:
        text = card_path.read_text(encoding="utf-8")
        for marker in SECTIONS:
            if f"<!-- BEGIN: {marker} -->" not in text:
                cards_missing_markers += 1
                print(f"  MISSING MARKER {marker} in {card_path.name}")
                break

    print("\n" + "=" * 60)
    print("SUMMARY")
    print("=" * 60)
    print(f"  refreshed     = {refreshed}")
    print(f"  new           = {new_created}")
    print(f"  data_sparse   = {data_sparse}")
    print(f"  total_cards   = {total_cards}")
    print(f"  missing_markers = {cards_missing_markers}")
    print(f"  content_loss_warnings = {content_loss_warnings}")

    # Idempotency check: re-run would produce zero changes
    print("\nIdempotency check (re-running on first 3 cards)...")
    sample_paths = list(existing_map.values())[:3]
    sample_ids = [int(p.stem.split("_")[0]) for p in sample_paths]
    idem_ok = True
    for pid, path in zip(sample_ids, sample_paths):
        before = path.read_text(encoding="utf-8")
        modified, _ = refresh_existing_card(path, pid, data)
        if modified:
            after = path.read_text(encoding="utf-8")
            if before != after:
                print(f"  IDEMPOTENCY FAIL: {path.name}")
                idem_ok = False
    if idem_ok:
        print("  All 3 samples: idempotent (zero byte-diff) OK")

    print("\nDone.")


if __name__ == "__main__":
    main()
